# Load a pre-trained model that supports Arabic
model = SentenceTransformer('paraphrase-multilingual-mpnet-base-v2')

embeddings = model.encode(poets)

with open('./data_folder/poet_embeddings.pkl', 'rb') as f:
    embeddings = pickle.load(f)
print("loaded embeddings")

# Normalize the embeddings
faiss.normalize_L2(embeddings)

# Create a FAISS index
index = faiss.IndexFlatIP(embeddings.shape[1])
index.add(embeddings)

def find_similar_poets(query, k=5):
    # Encode the query
    query_vector = model.encode([query])
    faiss.normalize_L2(query_vector)

    # Perform the search
    distances, indices = index.search(query_vector, k)

    # Return the results
    return [(poets[i], distances[0][j]) for j, i in enumerate(indices[0])]

# Example usage
query = "قصيدة عن حب الوطن لحافظ ابراهيم"
results = find_similar_poets(query)
print(f"Query: {query}")
print("Similar poets:")
for poet, score in results:
    print(f"{poet}: {score:.4f}")